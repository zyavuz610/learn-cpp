#include <iostream>
using namespace std;

int main() {
  cin>>n;
  for(int i=0;i<n;i++){
    for(int j=0;j<=n;j++){
      cout<<"*";
    }
    cout<<endl;
  }
/*
    - farklı türde üçgenleri ekrana basın
      kare, baklava dilimi, içi boş baklava dilimi, içi boş kare,
      üçgen (eşkenar, ikiz, dik)
    - kodda tek bir cout<<"*" komutu kullanarak yapmaya çalışın,
*/
  return 0;
}
